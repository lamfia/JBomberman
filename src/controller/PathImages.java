package controller;

/**
 * Classe che definisce i percorsi delle
 * immagini per le diverse direzioni di movimento
 * di un personaggio nel gioco.
 *
 * @author Gabriel Guerra
 */
public class PathImages {

    // Percorsi delle immagini per il movimento verso l'alto
    public String up1, up2, up3, up4, upidle;

    // Percorsi delle immagini per il movimento verso il basso
    public String down1, down2, down3, down4, downidle;

    // Percorsi delle immagini per il movimento verso sinistra
    public String left1, left2, left3, left4, leftidle;

    // Percorsi delle immagini per il movimento verso destra
    public String right1, right2, right3, right4, rightidle;
}
