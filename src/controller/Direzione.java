package controller;

/**
 * Enumerazione che rappresenta le direzioni possibili di movimento.
 * UP: verso l'alto
 * DOWN: verso il basso
 * RIGHT: verso destra
 * LEFT: verso sinistra
 * SPACE: spazio (usato per azioni speciali)
 *
 * @author Gabriel Guerra
 */
public enum Direzione {

    UP,
    DOWN,
    RIGHT,
    LEFT,
    SPACE
}
